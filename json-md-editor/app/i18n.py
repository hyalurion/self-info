SUPPORTED_LANGS = ["ja", "en", "zh-Hans", "zh-TW"]
DEFAULT_LANG = "zh-Hans"

# Map internal lang codes to friendly display (used in status bar etc.)
LANG_DISPLAY = {
    "ja": "日本語",
    "en": "English",
    "zh-Hans": "简体中文",
    "zh-TW": "繁體中文",
}

# Normalize alternate codes (e.g. changelog files use zh/tw) to canonical ones.
LANG_ALIASES = {
    "zh": "zh-Hans",
    "zh-cn": "zh-Hans",
    "zh-sg": "zh-Hans",
    "zh-my": "zh-Hans",
    "tw": "zh-TW",
    "zh-tw": "zh-TW",
    "zh-hant": "zh-TW",
    "hk": "zh-TW",
    "zh-hk": "zh-TW",
}


def normalize_lang(code: str | None) -> str:
    """Return a canonical supported lang code, or the default."""
    if not code:
        return DEFAULT_LANG
    c = str(code).strip().lower()
    if c in SUPPORTED_LANGS:
        return c
    if c in LANG_ALIASES:
        return LANG_ALIASES[c]
    # match by base language
    base = c.split("-")[0]
    if base in ("ja", "en"):
        return base
    if base == "zh":
        return "zh-Hans"
    return DEFAULT_LANG


class Translator:
    """Simple key-based translator with a single active language."""

    def __init__(self, lang: str = DEFAULT_LANG):
        self.lang = normalize_lang(lang)

    def set_lang(self, lang: str) -> None:
        self.lang = normalize_lang(lang)

    def available(self) -> list[str]:
        return list(SUPPORTED_LANGS)

    def get(self, key: str, **kwargs) -> str:
        table = STRINGS.get(self.lang, STRINGS["en"])
        text = table.get(key)
        if text is None:
            text = STRINGS["en"].get(key, key)
        if kwargs:
            try:
                text = text.format(**kwargs)
            except (KeyError, IndexError, ValueError):
                pass
        return text

    # Convenience alias.
    def t(self, key: str, **kwargs) -> str:
        return self.get(key, **kwargs)


# Global singleton — modules import ``tr`` and call ``t(key)``.
_tr = Translator()


def set_lang(lang: str) -> None:
    _tr.set_lang(lang)


def get_lang() -> str:
    return _tr.lang


def t(key: str, **kwargs) -> str:
    return _tr.get(key, **kwargs)
